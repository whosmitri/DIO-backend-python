
# arithmetic operators are used with numeric values to perform common mathematical operations

# addition ( + )
print(1 + 1)
# 2

# subtraction ( - )
print(10 - 2)
# 8

# multiplication ( * )
print(4 * 3)
# 12

# division ( / ) - returns a float
print(12 / 3)
# 4.0

# floor division ( // ) - returns an integer
print(12 // 2)
# 6

# modulus ( % )
print(10 % 3)
# 1

# exponentiation ( ** )
print(2 ** 3)
# 8

# precedency
print("Example 1:")
print(10 - 5 * 2)
# 0

print("Example 2:")
print((10 - 5) * 2)
# 5

print("Example 3:")
print(10 ** 2 * 2)
# 200

print("Example 4:")
print(10 ** (2 * 2))
# 10000

print("Example 5:")
print(10 / 2 * 4)
# 20.0