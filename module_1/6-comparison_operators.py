
x = 450
y = 200

# equal
print(x == y)
# False

# not equal
print(x != y)
# True

# greater than
print(x > y)
# True

# greater than or equal to
print(x >= y)
# True

# less than
print(x < y)
# False

# less than or equalto
print(x <= y)
# False

# chaining comparison operators
z = 10
print(1 < z < 20)
# True